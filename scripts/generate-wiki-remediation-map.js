#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const { buildWikiRegistry } = require('./wiki-content-state');

const ROOT = process.cwd();
const OUTPUT_JS = path.join(ROOT, 'cloudflare', 'wiki-url-remediation-map.js');
const OUTPUT_JSON = path.join(ROOT, 'cloudflare', 'wiki-url-remediation-map.json');

function writeMapFiles() {
  const registry = buildWikiRegistry();

  const redirects = Array.from(registry.redirects.entries())
    .map(([from, to]) => [from, to])
    .sort(([a], [b]) => a.localeCompare(b));
  const gone = Array.from(registry.retiredUrls).sort((a, b) => a.localeCompare(b));
  const gonePrefixes = Array.from(registry.retiredPrefixes).sort((a, b) => a.localeCompare(b));

  const js = `/**
 * Auto-generated by scripts/generate-wiki-remediation-map.js
 * Do not edit manually.
 */

export const WIKI_EXACT_301 = new Map(${JSON.stringify(redirects, null, 2)});
export const WIKI_EXACT_410 = new Set(${JSON.stringify(gone, null, 2)});
export const WIKI_410_PREFIXES = ${JSON.stringify(gonePrefixes, null, 2)};
`;

  const json = JSON.stringify(
    {
      generatedAt: new Date().toISOString(),
      exact301: redirects,
      exact410: gone,
      prefix410: gonePrefixes,
    },
    null,
    2
  );

  fs.writeFileSync(OUTPUT_JS, js, 'utf8');
  fs.writeFileSync(OUTPUT_JSON, `${json}\n`, 'utf8');

  console.log(`✓ Generated ${path.relative(ROOT, OUTPUT_JS)}`);
  console.log(`✓ Generated ${path.relative(ROOT, OUTPUT_JSON)}`);
  console.log(`  301: ${redirects.length}`);
  console.log(`  410 exact: ${gone.length}`);
  console.log(`  410 prefixes: ${gonePrefixes.length}`);
}

writeMapFiles();
